def emoji_replacer(input_string):
    output_string = ""
    i = 0

    # Convert input string to lowercase for case-insensitive matching
    input_string = input_string.lower()

    while i < len(input_string):
        # Take two characters to check for a valid flag
        if i < len(input_string) - 1:
            two_chars = input_string[i:i+2]
            if two_chars in flag_codes:
                output_string += f":flag_{two_chars}: "
                i += 2
                continue

        # If not a valid flag, add the first character as a regional indicator
        char = input_string[i]
        if char.isalnum():  # Check if the character is an alphabet
            output_string += f":regional_indicator_{char}: "
        else:
            # Add the character as it is (e.g., punctuation, numbers, etc.)
            output_string += char + " "

        i += 1

    # Remove trailing space if it exists
    output_string = output_string.rstrip()

    return output_string

